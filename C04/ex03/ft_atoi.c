#include <unistd.h>

int	ft_atoi(char *str)
{
	int	i;
	int	pow;
	int	s;

	i = 0;
	pow = 0;
	s = 1;
	while (str[i] == ' ' || (str[i] >= '\t' && str[i] <= '\r'))
	{
		i++;
	}
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
		{
			s *= -1;
		}
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		pow = (pow * 10) + str[i] - 48;
		i++;
	}
	return (pow * s);
}
